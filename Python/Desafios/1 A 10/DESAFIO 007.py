#D007 DESENVOLVA UM PROGRAMA QUE LEIA AS DUAS NOTAS DE UM ALUNO, CALCULE E MOSTRE A SUA MÉDIA.

n1 = float(input('Digite a sua primeira nota.'))
n2 = float(input('Digite a sua segunda nota.'))

m = (n1 + n2) / 2

print('A sua primeira nota foi {} , a sua segunda nota foi {} e a sua media foi {:.1f}'.format(n1, n2, m))


