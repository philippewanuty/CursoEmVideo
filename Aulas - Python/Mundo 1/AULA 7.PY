n1 = int(input('Quantas maçãs voce tem? ' ))
n2 = int(input('Quantas maçãs voce vai comprar ? '))	
ca = int(input('Quantas casas decimais deseja ? '))
s = n1 + n2 #SOMA
sb = n1 - n2 #SUBITRAÇÃO
m = n1 * n2 #MULTIPLICAÇÃO
d = n1 / n2 #DIVISÃO
di = n1 // n2 #DIVISÃO INTEIRA
e = n1 ** n2 #POTENCIA
r = n1 % n2 #RESTO

print('Você terá no total {} maçãs'.format(s)) # USANDO A VARIÁVEL
print('Você terá no total {} maçãs'.format(n1+n2)) # SEM USAR VARIÁVEL

print('A soma será {} , a subtração será {} , a multiplicação será {} , a divisão comum será {} a divisão inteira será {} e o resto será {} a potencia será {}'.format(s, sb, m, d, di, r, e))


